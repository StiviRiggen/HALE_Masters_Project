def is_water(utm_x, utm_y, MapData):
    """True if point lands on water""" 

    r, c = rowcol(MapData.wm_tfm, utm_x, utm_y, op=round)     # finds the pixel corrosponding to the coordinate
    # Out of bounds is water
    if r < 0 or c < 0 or r >= MapData.wm_arr.shape[0] or c >= MapData.wm_arr.shape[1]:
        return True

    water_check = MapData.wm_arr[r, c] == 1

    return water_check

def height_at_utm(x, y, MapData):
    """
    Function that takes a coordinate location in UTM and find the height value in the DTM.
    """
    if x < MapData.dtm_extent[0] or x > MapData.dtm_extent[1] or y < MapData.dtm_extent[2] or y > MapData.dtm_extent[3]:
        #print("Warning: Coordinate out of bounds for DTM. Returning NaN.")
        return np.nan
    elif np.isnan(x) or np.isnan(y):
        return np.nan
    else:
        row, col = rowcol(MapData.dtm_tfm, x, y, op=int) # find the pixel in the DTM that corrosponds to the coordinates
        height_val = MapData.dtm_arr[row, col]
    return height_val

    
def check_near_road(utm_x, utm_y, MapData):
    """Returns True if point is near road based on buffer distance.
    Returns a bearing to follow when follow road probability is met."""

    # Check if within map extent, if so return not near road.
    if utm_x < MapData.dtm_extent[0] or utm_x > MapData.dtm_extent[1] or utm_y < MapData.dtm_extent[2] or utm_y > MapData.dtm_extent[3]:
        return False
    
    # Check if near road by seeing if the agent is on the road buffer mask.
    r, c = rowcol(MapData.buff_roads_tfm, utm_x, utm_y, op=round)

    # If row and column are out of bounds, return not near road.
    if r < 0 or c < 0 or r >= MapData.buff_roads_arr.shape[0] or c >= MapData.buff_roads_arr.shape[1]:
        return False
    
    road_check = MapData.buff_roads_arr[r, c] == 1

    return road_check

