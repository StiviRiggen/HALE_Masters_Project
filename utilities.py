def DDA(transform, x0, y0, x1, y1, dda_resolution):

    dx = x1 - x0
    dy = y1 - y0

    if np.isnan(dx) or np.isnan(dy): # If either coordinate is NaN, return empty list (no cells) --> "fell_off_map" blocker should terminate the simulation on the next step.
        steps = 0
    else:
        steps = int(max(abs(dx), abs(dy))/dda_resolution) # 1 step per meter (1×1 raster)

    if steps == 0:
        r, c = rowcol(transform, x0, y0, op=round)
        return [(r, c)]

    x_inc = dx / steps
    y_inc = dy / steps

    cells = []
    prev = None

    x, y = x0, y0
    for _ in range(steps + 1):
        r, c = rowcol(transform, x, y, op=round)
        if prev != (r, c):
            cells.append((r, c))
            prev = (r, c)
        x += x_inc
        y += y_inc

    return cells




