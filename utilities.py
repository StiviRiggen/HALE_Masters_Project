def format_td(td):
    """Function to help formating the timing outputs.
    Removes decimal seconds from the output. (Generated by ChatGPT)"""
    total_seconds = int(td.total_seconds())  # removes decimals
    hours, remainder = divmod(total_seconds, 3600)
    minutes, seconds = divmod(remainder, 60)
    return f"{hours:02d}:{minutes:02d}:{seconds:02d}"

