def open_raster_UTM(src_path):
    """
    Load a raster and reproject to EPSG:4326 (lon/lat).
    Returns (arr, transform, crs), where arr is float32 with NaNs for NoData.
    Optionally set output resolution in degrees via dst_res_deg (e.g., 0.0001).
    """
    if src_path == None:
        print(f"Warning: No path provided for raster {src_path}. Returning None.")
        return None, None, None, None, None, None
    with rasterio.open(src_path) as src:
        src_arr = src.read(1)
        src_transform = src.transform
        src_crs = src.crs
        h, w = src.height, src.width
        src_extent = (src.bounds.left, src.bounds.right, src.bounds.bottom, src.bounds.top)
    return src_arr, src_transform, src_crs, src_extent, h, w

